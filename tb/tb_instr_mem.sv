`timescale 1ns/1ps

module tb_instr_mem;

    //--------------------------------------------------------------------------
    // Import Project Package
    //--------------------------------------------------------------------------

    import riscv_pkg::*;

    //--------------------------------------------------------------------------
    // DUT Signals
    //--------------------------------------------------------------------------

    xlen_t  addr_i;
    instr_t instr_o;

    //--------------------------------------------------------------------------
    // DUT Instance
    //--------------------------------------------------------------------------

    instr_mem u_instr_mem (

        .addr_i  (addr_i),
        .instr_o (instr_o)

    );

    //--------------------------------------------------------------------------
    // Waveform Dump
    //--------------------------------------------------------------------------

    initial begin
        $dumpfile("../waves/tb_instr_mem.vcd");
        $dumpvars(0, tb_instr_mem);
    end

    //--------------------------------------------------------------------------
    // Test Stimulus
    //--------------------------------------------------------------------------

    initial begin

        $display("");
        $display("==================================================");
        $display("      Instruction Memory Verification");
        $display("==================================================");

        //--------------------------------------------------------------
        // Fetch Instruction 0
        //--------------------------------------------------------------

        addr_i = 64'h0000_0008;
        #10;

        $display("ADDR = %h | INSTR = %h", addr_i, instr_o);

        //--------------------------------------------------------------
        // Fetch Instruction 1
        //--------------------------------------------------------------

        addr_i = 64'h0000_0004;
        #10;

        $display("ADDR = %h | INSTR = %h", addr_i, instr_o);

        //--------------------------------------------------------------
        // Fetch Instruction 2
        //--------------------------------------------------------------

        addr_i = 64'h0000_0008;
        #10;

        $display("ADDR = %h | INSTR = %h", addr_i, instr_o);

        //--------------------------------------------------------------
        // Fetch Instruction 3
        //--------------------------------------------------------------

        addr_i = 64'h0000_000C;
        #10;

        $display("ADDR = %h | INSTR = %h", addr_i, instr_o);

        //--------------------------------------------------------------
        // Fetch Instruction 4
        //--------------------------------------------------------------

        addr_i = 64'h0000_0010;
        #10;

        $display("ADDR = %h | INSTR = %h", addr_i, instr_o);

        //--------------------------------------------------------------
        // End Simulation
        //--------------------------------------------------------------

        $display("");
        $display("==================================================");
        $display(" Instruction Memory Test Completed Successfully");
        $display("==================================================");

        $finish;

    end

endmodule : tb_instr_mem
