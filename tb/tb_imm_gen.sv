`timescale 1ns/1ps

module tb_imm_gen;

    import riscv_pkg::*;

    //--------------------------------------------------------------------------
    // DUT Signals
    //--------------------------------------------------------------------------

    instr_t instr_i;

    xlen_t  imm_o;

    //--------------------------------------------------------------------------
    // DUT
    //--------------------------------------------------------------------------

    imm_gen u_imm_gen (

        .instr_i (instr_i),
        .imm_o   (imm_o)

    );

    //--------------------------------------------------------------------------
    // Waveform
    //--------------------------------------------------------------------------

    initial begin
        $dumpfile("../waves/tb_imm_gen.vcd");
        $dumpvars(0, tb_imm_gen);
    end

    //--------------------------------------------------------------------------
    // Test
    //--------------------------------------------------------------------------

    initial begin

        $display("");
        $display("========================================================");
        $display("          IMMEDIATE GENERATOR TESTBENCH");
        $display("========================================================");

        //--------------------------------------------------------------
        // ADDI x5,x6,10
        //--------------------------------------------------------------

        instr_i = 32'h00A30293;

        #10;

        $display("");
        $display("Instruction = %h", instr_i);
        $display("Immediate   = %h", imm_o);

        //--------------------------------------------------------------
        // ADDI x5,x6,-4
        //--------------------------------------------------------------

        instr_i = 32'hFFC30293;

        #10;

        $display("");
        $display("Instruction = %h", instr_i);
        $display("Immediate   = %h", imm_o);

        //--------------------------------------------------------------
        // Finish
        //--------------------------------------------------------------

        $display("");
        $display("========================================================");
        $display("Immediate Generator Test Completed Successfully");
        $display("========================================================");

        $finish;

    end

endmodule
