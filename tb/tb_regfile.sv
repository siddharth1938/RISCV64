//==============================================================================
// Project      : RISCV64 Processor
// Module       : Testbench - Register File
// File         : tb_regfile.sv
//
// Description  :
//   Testbench for verifying the Register File.
//
//==============================================================================

`timescale 1ns/1ps

module tb_regfile;

    import riscv_pkg::*;

    //--------------------------------------------------------------------------
    // Clock
    //--------------------------------------------------------------------------

    logic clk_i;

    //--------------------------------------------------------------------------
    // Read Port 1
    //--------------------------------------------------------------------------

    reg_addr_t rs1_addr_i;
    xlen_t     rs1_data_o;

    //--------------------------------------------------------------------------
    // Read Port 2
    //--------------------------------------------------------------------------

    reg_addr_t rs2_addr_i;
    xlen_t     rs2_data_o;

    //--------------------------------------------------------------------------
    // Write Port
    //--------------------------------------------------------------------------

    reg_addr_t rd_addr_i;
    xlen_t     rd_data_i;
    logic      rd_we_i;

    //--------------------------------------------------------------------------
    // DUT
    //--------------------------------------------------------------------------

    regfile dut (

        .clk_i      (clk_i),

        .rs1_addr_i (rs1_addr_i),
        .rs1_data_o (rs1_data_o),

        .rs2_addr_i (rs2_addr_i),
        .rs2_data_o (rs2_data_o),

        .rd_addr_i  (rd_addr_i),
        .rd_data_i  (rd_data_i),
        .rd_we_i    (rd_we_i)

    );

    //--------------------------------------------------------------------------
    // Clock Generation
    //--------------------------------------------------------------------------

    initial begin
        clk_i = 1'b0;
        forever #5 clk_i = ~clk_i;
    end

    //--------------------------------------------------------------------------
    // Waveform
    //--------------------------------------------------------------------------

    initial begin
        $dumpfile("../waves/tb_regfile.vcd");
        $dumpvars(0, tb_regfile);
    end

    //--------------------------------------------------------------------------
    // Monitor
    //--------------------------------------------------------------------------

    initial begin

        $display("");
        $display("========================================================");
        $display("            REGISTER FILE TESTBENCH");
        $display("========================================================");

        $display("TIME   WE  RD   DATA                 RS1 RS1_DATA               RS2 RS2_DATA");

        $monitor("%5t   %0b   %2d   %h   %2d  %h   %2d  %h",
                 $time,
                 rd_we_i,
                 rd_addr_i,
                 rd_data_i,
                 rs1_addr_i,
                 rs1_data_o,
                 rs2_addr_i,
                 rs2_data_o);

    end

    //--------------------------------------------------------------------------
    // Test Sequence
    //--------------------------------------------------------------------------

    initial begin

        //--------------------------------------------------------------
        // Initialize
        //--------------------------------------------------------------

        rd_we_i    = 1'b0;
        rd_addr_i  = '0;
        rd_data_i  = '0;

        rs1_addr_i = '0;
        rs2_addr_i = '0;

        //--------------------------------------------------------------
        // Write x5 = 100
        //--------------------------------------------------------------

        @(posedge clk_i);

        rd_we_i   = 1'b1;
        rd_addr_i = 5;
        rd_data_i = 64'd100;

        @(posedge clk_i);

        rd_we_i = 1'b0;

        //--------------------------------------------------------------
        // Read x5
        //--------------------------------------------------------------

        rs1_addr_i = 5;

        #10;

        //--------------------------------------------------------------
        // Write x10 = 200
        //--------------------------------------------------------------

        @(posedge clk_i);

        rd_we_i   = 1'b1;
        rd_addr_i = 10;
        rd_data_i = 64'd200;

        @(posedge clk_i);

        rd_we_i = 1'b0;

        //--------------------------------------------------------------
        // Read x10
        //--------------------------------------------------------------

        rs2_addr_i = 10;

        #10;

        //--------------------------------------------------------------
        // Try writing x0 (should be ignored)
        //--------------------------------------------------------------

        @(posedge clk_i);

        rd_we_i   = 1'b1;
        rd_addr_i = 0;
        rd_data_i = 64'hFFFF_FFFF_FFFF_FFFF;

        @(posedge clk_i);

        rd_we_i = 1'b0;

        rs1_addr_i = 0;

        #10;

        //--------------------------------------------------------------
        // Self Checks
        //--------------------------------------------------------------

        if (rs1_data_o !== 64'd0) begin
            $fatal(1, "ERROR: x0 Register Modified!");
        end

        rs1_addr_i = 5;
        #10;

        if (rs1_data_o !== 64'd100) begin
            $fatal(1, "ERROR: x5 Readback Failed!");
        end

        rs2_addr_i = 10;
        #10;

        if (rs2_data_o !== 64'd200) begin
            $fatal(1, "ERROR: x10 Readback Failed!");
        end

        //--------------------------------------------------------------
        // PASS
        //--------------------------------------------------------------

        $display("");
        $display("========================================================");
        $display("      REGISTER FILE TEST PASSED SUCCESSFULLY");
        $display("========================================================");

        $finish;

    end

endmodule : tb_regfile
