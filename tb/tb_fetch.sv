`timescale 1ns/1ps

//------------------------------------------------------------------------------
// Import Project Package
//------------------------------------------------------------------------------


//==============================================================================
// Testbench
//==============================================================================

module tb_fetch;

    import riscv_pkg::*;

    //--------------------------------------------------------------------------
    // Clock and Reset
    //--------------------------------------------------------------------------

    logic clk_i;
    logic rst_ni;

    //--------------------------------------------------------------------------
    // Inputs
    //--------------------------------------------------------------------------

    logic  load_pc_i;
    xlen_t next_pc_i;

    //--------------------------------------------------------------------------
    // Outputs
    //--------------------------------------------------------------------------

    xlen_t  pc_o;
    instr_t instr_o;

    //--------------------------------------------------------------------------
    // DUT
    //--------------------------------------------------------------------------

    fetch dut (

        .clk_i       (clk_i),
        .rst_ni      (rst_ni),

        

        .load_pc_i   (load_pc_i),
        .next_pc_i   (next_pc_i),

        .pc_o        (pc_o),
        .instr_o     (instr_o)

    );

    //--------------------------------------------------------------------------
    // Clock Generation
    //--------------------------------------------------------------------------

    initial begin
        clk_i = 1'b0;
        forever #5 clk_i = ~clk_i;
    end

    //--------------------------------------------------------------------------
    // Waveform
    //--------------------------------------------------------------------------

    initial begin
        $dumpfile("../waves/tb_fetch.vcd");
        $dumpvars(0, tb_fetch);
    end

    //--------------------------------------------------------------------------
    // Monitor
    //--------------------------------------------------------------------------

    initial begin
        $display("");
        $display("==============================================================");
        $display("                 FETCH UNIT SIMULATION");
        $display("==============================================================");
        $display(" TIME      PC                  INSTRUCTION");
        $display("--------------------------------------------------------------");

        $monitor("%5t   %h   %h",
                 $time,
                 pc_o,
                 instr_o);
    end

    //--------------------------------------------------------------------------
    // Test Stimulus
    //--------------------------------------------------------------------------

    initial begin

        //--------------------------------------------------------------
        // Reset
        //--------------------------------------------------------------

        rst_ni      = 1'b0;
        load_pc_i   = 1'b0;
        next_pc_i   = '0;

        #20;

        //--------------------------------------------------------------
        // Release Reset
        //--------------------------------------------------------------

        rst_ni = 1'b1;

        //--------------------------------------------------------------
        // Sequential Fetch
        //--------------------------------------------------------------

        repeat (5)
            @(posedge clk_i);

        //--------------------------------------------------------------
        // Branch / Jump
        //--------------------------------------------------------------

        load_pc_i = 1'b1;
        next_pc_i = RESET_VECTOR + xlen_t'(64'h100);

        @(posedge clk_i);

        load_pc_i = 1'b0;

        //--------------------------------------------------------------
        // Continue Fetch
        //--------------------------------------------------------------

        repeat (5)
            @(posedge clk_i);

        //--------------------------------------------------------------
        // Finish
        //--------------------------------------------------------------

        $display("");
        $display("==============================================================");
        $display("            FETCH UNIT TEST COMPLETED SUCCESSFULLY");
        $display("==============================================================");

        $finish;

    end

endmodule : tb_fetch
