`timescale 1ns/1ps

//==============================================================================
// Testbench
//==============================================================================

module tb_pc;

    //--------------------------------------------------------------------------
    // Import Project Package
    //--------------------------------------------------------------------------

    import riscv_pkg::*;

    //--------------------------------------------------------------------------
    // Clock and Reset
    //--------------------------------------------------------------------------

    logic clk_i;
    logic rst_ni;

    //--------------------------------------------------------------------------
    // DUT Inputs
    //--------------------------------------------------------------------------

    logic  load_pc_i;
    xlen_t next_pc_i;

    //--------------------------------------------------------------------------
    // DUT Outputs
    //--------------------------------------------------------------------------

    xlen_t pc_o;

    //--------------------------------------------------------------------------
    // Instantiate DUT
    //--------------------------------------------------------------------------

    pc dut (

        .clk_i     (clk_i),
        .rst_ni    (rst_ni),

        .load_pc_i (load_pc_i),
        .next_pc_i (next_pc_i),

        .pc_o      (pc_o)

    );

    //--------------------------------------------------------------------------
    // Clock Generation
    //--------------------------------------------------------------------------

    initial begin
        clk_i = 1'b0;

        forever #5 clk_i = ~clk_i;
    end

    //--------------------------------------------------------------------------
    // Waveform Dump
    //--------------------------------------------------------------------------

    initial begin
        $dumpfile("../waves/tb_pc.vcd");
        $dumpvars(0, tb_pc);
    end

    //--------------------------------------------------------------------------
    // Simulation Monitor
    //--------------------------------------------------------------------------

    always @(posedge clk_i) begin
        $display("TIME=%0t | PC=%h | LOAD=%0b | NEXT_PC=%h",
                 $time,
                 pc_o,
                 load_pc_i,
                 next_pc_i);
    end

    //--------------------------------------------------------------------------
    // Test Stimulus
    //--------------------------------------------------------------------------

    initial begin

        //----------------------------------------------------------------------
        // Initialize Inputs
        //----------------------------------------------------------------------

        rst_ni    = 1'b0;
        load_pc_i = 1'b0;
        next_pc_i = '0;

        //----------------------------------------------------------------------
        // Hold Reset
        //----------------------------------------------------------------------

        #20;

        //----------------------------------------------------------------------
        // Release Reset
        //----------------------------------------------------------------------

        rst_ni = 1'b1;

        //----------------------------------------------------------------------
        // Normal Sequential Execution
        //----------------------------------------------------------------------

        repeat (5) @(posedge clk_i);

        //----------------------------------------------------------------------
        // Program Counter Redirection
        //----------------------------------------------------------------------

        load_pc_i = 1'b1;
        next_pc_i = 64'h8000_0100;

        @(posedge clk_i);

        load_pc_i = 1'b0;

        //----------------------------------------------------------------------
        // Continue Sequential Execution
        //----------------------------------------------------------------------

        repeat (5) @(posedge clk_i);

        //----------------------------------------------------------------------
        // Test Complete
        //----------------------------------------------------------------------

        $display("");
        $display("=======================================================");
        $display("      PROGRAM COUNTER TEST PASSED SUCCESSFULLY");
        $display("=======================================================");
        $display("");

        $finish;

    end

endmodule : tb_pc
