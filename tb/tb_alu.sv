// Description  :
//   Testbench for verifying the Arithmetic Logic Unit.
//
//==============================================================================

`timescale 1ns/1ps

module tb_alu;

    import riscv_pkg::*;
    import riscv_alu_pkg::*;

    //--------------------------------------------------------------------------
    // DUT Signals
    //--------------------------------------------------------------------------

    xlen_t   operand_a_i;
    xlen_t   operand_b_i;
    alu_op_t alu_op_i;

    xlen_t   result_o;
    logic    zero_o;

    //--------------------------------------------------------------------------
    // DUT
    //--------------------------------------------------------------------------

    alu dut (

        .operand_a_i (operand_a_i),
        .operand_b_i (operand_b_i),
        .alu_op_i    (alu_op_i),

        .result_o    (result_o),
        .zero_o      (zero_o)

    );

    //--------------------------------------------------------------------------
    // Waveform
    //--------------------------------------------------------------------------

    initial begin
        $dumpfile("../waves/tb_alu.vcd");
        $dumpvars(0, tb_alu);
    end

    //--------------------------------------------------------------------------
    // Test Sequence
    //--------------------------------------------------------------------------

    initial begin

        $display("");
        $display("========================================================");
        $display("               ALU TESTBENCH");
        $display("========================================================");

        //--------------------------------------------------------------
        // ADD
        //--------------------------------------------------------------

        operand_a_i = 10;
        operand_b_i = 20;
        alu_op_i    = ALU_ADD;

        #10;

        if (result_o != 30)
            $fatal(1,"ADD FAILED");

        //--------------------------------------------------------------
        // SUB
        //--------------------------------------------------------------

        alu_op_i = ALU_SUB;

        #10;

        if (result_o != -10)
            $fatal(1,"SUB FAILED");

        //--------------------------------------------------------------
        // AND
        //--------------------------------------------------------------

        operand_a_i = 64'hF0;
        operand_b_i = 64'hCC;
        alu_op_i    = ALU_AND;

        #10;

        if (result_o != 64'hC0)
            $fatal(1,"AND FAILED");

        //--------------------------------------------------------------
        // OR
        //--------------------------------------------------------------

        alu_op_i = ALU_OR;

        #10;

        if (result_o != 64'hFC)
            $fatal(1,"OR FAILED");

        //--------------------------------------------------------------
        // XOR
        //--------------------------------------------------------------

        alu_op_i = ALU_XOR;

        #10;

        if (result_o != 64'h3C)
            $fatal(1,"XOR FAILED");

        //--------------------------------------------------------------
        // SLL
        //--------------------------------------------------------------

        operand_a_i = 1;
        operand_b_i = 4;
        alu_op_i    = ALU_SLL;

        #10;

        if (result_o != 16)
            $fatal(1,"SLL FAILED");

        //--------------------------------------------------------------
        // SRL
        //--------------------------------------------------------------

        operand_a_i = 64'h80;
        operand_b_i = 3;
        alu_op_i    = ALU_SRL;

        #10;

        if (result_o != 64'h10)
            $fatal(1,"SRL FAILED");

        //--------------------------------------------------------------
        // SRA
        //--------------------------------------------------------------

        operand_a_i = -64;
        operand_b_i = 2;
        alu_op_i    = ALU_SRA;

        #10;

        if ($signed(result_o) != -16)
            $fatal(1,"SRA FAILED");

        //--------------------------------------------------------------
        // SLT
        //--------------------------------------------------------------

        operand_a_i = -5;
        operand_b_i = 2;
        alu_op_i    = ALU_SLT;

        #10;

        if (result_o != 1)
            $fatal(1,"SLT FAILED");

        //--------------------------------------------------------------
        // SLTU
        //--------------------------------------------------------------

        operand_a_i = 5;
        operand_b_i = 10;
        alu_op_i    = ALU_SLTU;

        #10;

        if (result_o != 1)
            $fatal(1,"SLTU FAILED");

        //--------------------------------------------------------------
        // ZERO FLAG
        //--------------------------------------------------------------

        operand_a_i = 20;
        operand_b_i = 20;
        alu_op_i    = ALU_SUB;

        #10;

        if (!zero_o)
            $fatal(1,"ZERO FLAG FAILED");

        //--------------------------------------------------------------
        // PASS
        //--------------------------------------------------------------

        $display("");
        $display("========================================================");
        $display("         ALU TEST PASSED SUCCESSFULLY");
        $display("========================================================");

        $finish;

    end

endmodule : tb_alu
