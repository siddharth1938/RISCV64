`timescale 1ns/1ps

module alu
    import riscv_pkg::*;
    import riscv_alu_pkg::*;
(

    //--------------------------------------------------------------------------
    // Operands
    //--------------------------------------------------------------------------

    input  xlen_t   operand_a_i,
    input  xlen_t   operand_b_i,

    //--------------------------------------------------------------------------
    // ALU Operation
    //--------------------------------------------------------------------------

    input  alu_op_t alu_op_i,

    //--------------------------------------------------------------------------
    // Outputs
    //--------------------------------------------------------------------------

    output xlen_t   result_o,
    output logic    zero_o

);

    //==========================================================================
    // Internal Result Register
    //==========================================================================

    xlen_t result;

    //==========================================================================
    // Combinational ALU
    //==========================================================================

    always_comb begin

        //--------------------------------------------------------------
        // Default Output
        //--------------------------------------------------------------

        result = '0;

        //--------------------------------------------------------------
        // ALU Operations
        //--------------------------------------------------------------

        unique case (alu_op_i)

            //----------------------------------------------------------
            // Addition
            //----------------------------------------------------------

            ALU_ADD:
                result = operand_a_i + operand_b_i;

            //----------------------------------------------------------
            // Subtraction
            //----------------------------------------------------------

            ALU_SUB:
                result = operand_a_i - operand_b_i;

            //----------------------------------------------------------
            // Bitwise AND
            //----------------------------------------------------------

            ALU_AND:
                result = operand_a_i & operand_b_i;

            //----------------------------------------------------------
            // Bitwise OR
            //----------------------------------------------------------

            ALU_OR:
                result = operand_a_i | operand_b_i;

            //----------------------------------------------------------
            // Bitwise XOR
            //----------------------------------------------------------

            ALU_XOR:
                result = operand_a_i ^ operand_b_i;

            //----------------------------------------------------------
            // Logical Left Shift
            //----------------------------------------------------------

            ALU_SLL:
                result = operand_a_i << operand_b_i[5:0];

            //----------------------------------------------------------
            // Logical Right Shift
            //----------------------------------------------------------

            ALU_SRL:
                result = operand_a_i >> operand_b_i[5:0];

            //----------------------------------------------------------
            // Arithmetic Right Shift
            //----------------------------------------------------------

            ALU_SRA:
                result = $signed(operand_a_i) >>> operand_b_i[5:0];

            //----------------------------------------------------------
            // Signed Less Than
            //----------------------------------------------------------

            ALU_SLT:
                result = ($signed(operand_a_i) < $signed(operand_b_i))
                         ? xlen_t'(1)
                         : xlen_t'(0);

            //----------------------------------------------------------
            // Unsigned Less Than
            //----------------------------------------------------------

            ALU_SLTU:
                result = (operand_a_i < operand_b_i)
                         ? xlen_t'(1)
                         : xlen_t'(0);

            //----------------------------------------------------------
            // Default
            //----------------------------------------------------------

            default:
                result = '0;

        endcase

    end

    //==========================================================================
    // Output Logic
    //==========================================================================

    assign result_o = result;

    assign zero_o = (result == '0);

endmodule : alu
