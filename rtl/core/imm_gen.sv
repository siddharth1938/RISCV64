//==============================================================================
// Project      : RISCV64 Processor
// Module       : Immediate Generator
// File         : imm_gen.sv
//
// Description  :
//   Generates the sign-extended immediate value from a 32-bit RISC-V
//   instruction.
//
//   Supported Formats:
//     - I-Type
//     - S-Type
//     - B-Type
//     - U-Type
//     - J-Type
//
//------------------------------------------------------------------------------
// Author       : Siddhartha Chinta
// Organization : Personal Learning Project
//
// Reference    :
//   - RISC-V Unprivileged ISA Specification
//
// Target ISA   : RV64I
// Language     : SystemVerilog
//==============================================================================

`timescale 1ns/1ps

module imm_gen
    import riscv_pkg::*;
    import riscv_opcode_pkg::*;
(

    //--------------------------------------------------------------------------
    // Input Instruction
    //--------------------------------------------------------------------------

    input  instr_t instr_i,

    //--------------------------------------------------------------------------
    // Immediate Output
    //--------------------------------------------------------------------------

    output xlen_t imm_o

);

    //==========================================================================
    // Internal Signals
    //==========================================================================

    opcode_t opcode;

    //==========================================================================
    // Opcode Extraction
    //==========================================================================

    assign opcode = instr_i[6:0];

    //==========================================================================
    // Immediate Generation
    //==========================================================================

    always_comb begin

        // Default
        imm_o = '0;

        case (opcode)

            //------------------------------------------------------------------
            // I-Type
            //------------------------------------------------------------------

            OPCODE_LOAD,
            OPCODE_OP_IMM,
            OPCODE_JALR :

                imm_o = {{52{instr_i[31]}},
                          instr_i[31:20]};

            //------------------------------------------------------------------
            // S-Type
            //------------------------------------------------------------------

            OPCODE_STORE :

                imm_o = {{52{instr_i[31]}},
                          instr_i[31:25],
                          instr_i[11:7]};

            //------------------------------------------------------------------
            // B-Type
            //------------------------------------------------------------------

            OPCODE_BRANCH :

                imm_o = {{51{instr_i[31]}},
                          instr_i[31],
                          instr_i[7],
                          instr_i[30:25],
                          instr_i[11:8],
                          1'b0};

            //------------------------------------------------------------------
            // U-Type
            //------------------------------------------------------------------

            OPCODE_LUI,
            OPCODE_AUIPC :

                imm_o = {{32{instr_i[31]}},
                          instr_i[31:12],
                          12'b0};

            //------------------------------------------------------------------
            // J-Type
            //------------------------------------------------------------------

            OPCODE_JAL :

                imm_o = {{43{instr_i[31]}},
                          instr_i[31],
                          instr_i[19:12],
                          instr_i[20],
                          instr_i[30:21],
                          1'b0};

            //------------------------------------------------------------------
            // Default
            //------------------------------------------------------------------

            default :

                imm_o = '0;

        endcase

    end

endmodule : imm_gen
